    private void addKeyListenerTo_md_jTextFieldUsername(){
        md_jTextFieldUsername.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
                char znak = e.getKeyChar();
                int kod = (int)znak;                
                
                if (kod >= 97 && kod <= 199 || kod >= 65 && kod <= 89 || znak == KeyEvent.VK_BACK_SPACE){
                    md_jTextFieldUsername.setEditable(true);
                }
                else{
                    md_jTextFieldUsername.setEditable(false);
                }
            }

            @Override
            public void keyPressed(KeyEvent e) {

            }

            @Override
            public void keyReleased(KeyEvent e) {

            }
        });
    }
    
    private void addKeyListernerTo_md_jPasswordField(){
        md_jPasswordFieldRejestracja.addKeyListener(new KeyListener() {
            @Override
            public void keyTyped(KeyEvent e) {
               char znak = e.getKeyChar();
               int kod = (int) znak;
               
               if (kod >= 97 && kod <= 199 || kod >= 65 && kod <= 89 || znak == KeyEvent.VK_BACK_SPACE || znak >= '0' && znak <= '9'){
                   md_jPasswordFieldRejestracja.setEditable(true);
               } else{
                   md_jPasswordFieldRejestracja.setEditable(false);
               }
            }

            @Override
            public void keyPressed(KeyEvent e) {
                
            }

            @Override
            public void keyReleased(KeyEvent e) {
                
            }
        });
    }
    
