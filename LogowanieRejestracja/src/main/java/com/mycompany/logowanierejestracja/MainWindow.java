    public String readFromFile(){
        //Pobieranie danych z p√≥l do zmiennych
        hasloLogowanie = String.valueOf(md_jPasswordField.getPassword());
        emailLogowanie = md_jTextFieldEmail.getText();
        
        //Przygotowanie zmiennych w odpowiednim formacie do zapisu
        String daneLogowanie = emailLogowanie+";"+hasloLogowanie;
        System.out.println(daneLogowanie);
        
        String message = "";
        
        //Pr√≥ba zapisu do pliku
        Scanner sc;
        try {
            sc = new Scanner(new File(plik));
            while (sc.hasNext()){
            String aktualnaLinia = sc.nextLine();
            String[] parts = aktualnaLinia.split(";");
            String username = parts[0];
            String email = parts[1];
            String password = parts[2];
            
            String help1 = email+";"+password;
            System.out.println("Help1 = "+help1);
            System.out.println(aktualnaLinia);
            
                if (daneLogowanie.equals(help1)){
                    JOptionPane.showMessageDialog(null, "Uda≈Ço ci siƒô zalogowaƒá! Witaj: "+username);
                }else{
                    JOptionPane.showMessageDialog(null, "Nie uda≈Ço ci siƒô zalogowaƒá!");
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        return message;
    }
    
