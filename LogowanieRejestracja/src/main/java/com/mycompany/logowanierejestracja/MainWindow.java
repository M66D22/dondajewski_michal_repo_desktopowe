    private void md_jButtonRejestracjaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_md_jButtonRejestracjaActionPerformed
        //Sprawdzanie czy dane sƒÖ prawid≈Çowe
        if (md_jTextFieldUsername.getText().length() > 20 || md_jTextFieldUsername.getText().length() < 2){
            JOptionPane.showMessageDialog(null, "Pole username jest ≈∫le wype≈Çnione!");
            sprawdzanie += 1;
        }
        String password = String.valueOf(md_jPasswordFieldRejestracja.getPassword());
        System.out.println(password);
        if (!Arrays.equals(md_jPasswordFieldRejestracja.getPassword(), md_jConfirmPasswordField.getPassword())){
            JOptionPane.showMessageDialog(null, "Hasla nie sa takie same");
            sprawdzanie += 1;
        }else{
            System.out.println("Hasla sa takie same");
        }
        char [] passChar = md_jPasswordFieldRejestracja.getPassword();
        
        if (passChar.length < 4){
            JOptionPane.showMessageDialog(null, "W hasle jest za malo liter");
            sprawdzanie += 1;
        }   
        
        //Pobieranie danych do zmiennych
        username = md_jTextFieldUsername.getText();
        email = md_jTextFieldEmailRejestracja.getText();
        String zapis = username+";"+email+";"+password;
        //String zapisDoLogowania = email+";"+password;
        System.out.println(zapis);
        
        //Walidacja Email
        Pattern pat = Pattern.compile(".+@.+\\.[a-z]+");
        Matcher match = pat.matcher(email);
        
        boolean matchFound = match.matches();
        
        if (matchFound == true){
            System.out.println("Poprawny email");
        }else{
            JOptionPane.showMessageDialog(null, "Podany adres email nie jest prawid≈Çowy");
            sprawdzanie += 1;
        }
        
        //Czyszczenie p√≥l
        md_jTextFieldEmailRejestracja.setText("");
        md_jTextFieldUsername.setText("");
        md_jPasswordFieldRejestracja.setText("");
        md_jConfirmPasswordField.setText("");
        
        //Pr√≥ba zapisu do pliku
        if(sprawdzanie == 0){
            try {
                FileWriter fw = new FileWriter(plik, true);
                fw.write(zapis+"\n");
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            sprawdzanie = 0;
        }
    }//GEN-LAST:event_md_jButtonRejestracjaActionPerformed
    
